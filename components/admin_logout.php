<?php

// 1. දත්ත සමුදාය (Database) සමඟ සම්බන්ධ වීමට අවශ්‍ය ගොනුව ඇතුළත් කර ගනී.
include 'connect.php';

// 2. දැනට පවතින Session එක ආරම්භ කරයි. (පරිශීලකයා ලොග් වී ඇති බව හඳුනා ගැනීමට මෙය අවශ්‍ය වේ).
session_start();

// 3. දැනට Session එකේ ඇති සියලුම දත්ත (variables) ඉවත් කරයි (Unset).
session_unset();

// 4. Session එක සම්පූර්ණයෙන්ම විනාශ කර දමයි. මෙයින් පරිශීලකයා පද්ධතියෙන් සාර්ථකව ඉවත් වේ.
session_destroy();

// 5. ලොග් අවුට් වීමෙන් පසු පරිපාලකයා නැවත ලොගින් පිටුව (Login Page) වෙත ස්වයංක්‍රීයව යොමු කරයි.
header('location:../admin/admin_login.php');

?>