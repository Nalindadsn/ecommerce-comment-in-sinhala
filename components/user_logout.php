<?php

// 1. දත්ත සමුදාය සම්බන්ධතාවය (Database Connection) ඇතුළත් කර ගැනීම.
include 'connect.php';

// 2. දැනට ක්‍රියාත්මක වන පරිශීලක වාරය (Session) ආරම්භ කිරීම හෝ හඳුනා ගැනීම.
// ඕනෑම Session දත්තයක් හැසිරවීමට පෙර මෙය අනිවාර්ය වේ.
session_start();

// 3. දැනට පද්ධතිය තුළ ගබඩා කර ඇති සියලුම Session විචල්‍යයන් (Variables) ඉවත් කිරීම.
// මෙහිදී user_id වැනි තොරතුරු හිස් කරනු ලබයි.
session_unset();

// 4. වත්මන් පරිශීලක වාරය (Session) සම්පූර්ණයෙන්ම විනාශ කර දැමීම.
// මෙයින් පසු පරිශීලකයා ලොග් වී ඇති බව පද්ධතිය හඳුනා නොගනී.
session_destroy();

// 5. පරිශීලකයා පද්ධතියෙන් ඉවත් වූ පසු නැවත වෙබ් අඩවියේ මුල් පිටුවට (home.php) යොමු කිරීම.
header('location:../home.php');

?>